package com.jercodes.SuperSigmaProject.components.magic;

public class MagicCompositionComponent {
	private int black;
	private int purple;
	private int brown;
	private int yellow;
	private int blue;
	private int red;
	private int green;
	private int white;
	
	public MagicCompositionComponent(int black, int purple, int brown, int yellow, int blue, int red, int green, int white) {
		super();
		this.black = black;
		this.purple = purple;
		this.brown = brown;
		this.yellow = yellow;
		this.blue = blue;
		this.red = red;
		this.green = green;
		this.white = white;
	}

	public int getBlack() {
		return black;
	}

	public int getPurple() {
		return purple;
	}

	public int getBrown() {
		return brown;
	}

	public int getYellow() {
		return yellow;
	}

	public int getBlue() {
		return blue;
	}

	public int getRed() {
		return red;
	}

	public int getGreen() {
		return green;
	}

	public int getWhite() {
		return white;
	}
	
}
